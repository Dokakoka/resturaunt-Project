        String pass = password.getText();
        String comboBox = roll_id.getSelectedItem().toString();
        
        if(name.equals("") || pass.equals("")){
            JOptionPane.showMessageDialog(null,"You should enter all fields", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        else{
        Login l = new Login();
        int Roll_id = l.Roll(comboBox);
        
      
        try {
            if(Roll_id == 3) {
            l.customerLogin(name, pass);
        } else {
            l.sigin(name, pass, Roll_id);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,ex, "Error", JOptionPane.ERROR_MESSAGE);
        }
        this.setVisible(false);
        }        
